#[derive(Debug, Clone, PartialEq)]
pub enum Ops {
    Stop,
    Nop,
    Read,
    Print,
    Write(String),
    Store(usize),
    Const(f32),
    Recall(Option<usize>),
    Swap(usize),
    Copy(usize),
    Add(usize),
    Sub(usize),
    Mul(usize),
    Div(usize),
    Sign,
    Abs,
    Sqrt,
    Exp,
    Log,
    Sin,
    Cos,
    Tan,
    Floor,
    Ceil,
    Trunc,
    Round,
    Rand,
    Jump(Option<usize>),
    Positive(usize),
    Negative(usize),
    Zero(usize),
}

pub type Program = Vec<Ops>;
